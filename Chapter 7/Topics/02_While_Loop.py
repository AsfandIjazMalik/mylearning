a = 1

while(a<=10): # The block keeps executing until the condition is true 
    print("The a is now",a) #Body of the loop
    # increment of 1
    a +=1  # a = a + 1

# In while loops, the condition is checked first. If it evaluates to true,
# the body of the loop is executed otherwise not!

# If the loop is entered, the process of [condition check & execution] is
# continued until the condition becomes False.