number = [3,2,4,1,9,5,7,6,8,10]
print(type(number))
number.sort() # arranging the numbers from 1 to 10
print(number)  

number.reverse() # arranging the numbers from 10 to 1
print(number)

number.insert(3, 11) # it will add 11 at index 3
print(number)

number.pop(0) # delete digit at index 0
print(number)

number.remove(9) # remove 9 from the list
print(number)
