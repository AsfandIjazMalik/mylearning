print("WHAT IS STRING IN PYTHON")

# String is a data type in python.
# String is a sequence of characters enclosed in quotes.
# Strings are immutable
# Cannot be change an existing string

# We can primarily write a string in these three ways.

first_name = 'Asfand'                 # In SINGLE quotes
middle_name =  "Ijaz"                 # In DOUBLE Quotes
full_name = '''Asfand Ijaz Malik'''   # In TRIPLE SINGLE Quotes
last_name = """Malik"""               # In TRIPLE DOUBLE Quotes 
print(first_name,middle_name,last_name,full_name)

